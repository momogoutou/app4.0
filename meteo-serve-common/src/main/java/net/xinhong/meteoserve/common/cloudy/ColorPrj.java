package net.xinhong.meteoserve.common.cloudy;

public class ColorPrj {
	public int Alpha;
	public int R;
	public int G;
	public int B;
	public ColorPrj(int alpha, int r, int g, int b) {
		super();
		Alpha = alpha;
		R = r;
		G = g;
		B = b;
	}
	public int getAlpha() {
		return Alpha;
	}
	public void setAlpha(int alpha) {
		Alpha = alpha;
	}
	public int getR() {
		return R;
	}
	public void setR(int r) {
		R = r;
	}
	public int getG() {
		return G;
	}
	public void setG(int g) {
		G = g;
	}
	public int getB() {
		return B;
	}
	public void setB(int b) {
		B = b;
	}
	
	
}
